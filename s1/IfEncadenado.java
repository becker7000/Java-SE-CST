public class IfEncadenado {

    public static void main(String[] args) {

        //Un número puede ser negativo, cero o positivo.
        int x=10;

        if(x>0)
            System.out.println("\n\t El número es positivo");
        else if(x<0)
            System.out.println("\n\t El número es negativo");
        else
            System.out.println("\n\t El número es exactamente cero.");

    }

}
