public class OperadoresAsignacion {

    public static void main(String[] args) {

        int ahorro=0;
        ahorro+=100;
        ahorro+=180;
        ahorro*=2;
        ahorro+=200;
        ahorro-=100;
        ahorro+=300;

        System.out.println("\n\t El ahorro final es: "+ahorro);
    }

}
