public class Variables {

    public static void main(String[] args) {

        int x=5*(3/2); //5*(1.5)=7.5   3/2 = 1 <- entera   , 3/2 =1.5 <- exacta
        System.out.println("x = "+x);
        //Cuando usamos '+' dentro de println sirve para concatenar cadenas.

        double y=15f/4f; //Variable de tipo double.
        System.out.println("y = "+y);

    }

}
